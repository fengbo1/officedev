package office.zcgl.pojo;

public class SearchInfoBean {
  private String name;
  private String type;
  private int chu;
  private int status;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public int getChu() {
	return chu;
}
public void setChu(int chu) {
	this.chu = chu;
}
public int getStatus() {
	return status;
}
public void setStatus(int status) {
	this.status = status;
}
  
}
